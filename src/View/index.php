                <p>IT’S FLU VACCINE season, as you might be able to tell as you go about your business today judging by the increase in polite coughs and sniffles about the place.</p>
<p>Yesterday, Health Minister Simon Harris said that medical workers in Ireland are under an obligation to get the flu vaccine:</p>
<p>“You will be coming in contact with sick people during a period when the flu can become quite an issue. You have an obligation to your patients I believe to get the vaccination.”</p>
<p></p>
<p>Uptake of the vaccine for such workers is “appallingly low” according to Harris. Just 31% of hospital workers here were immunised last year.</p>
<p>Now, Harris says that patients have a right to ask their doctor or nurse whether or not they have received the vaccine themselves.</p>
<p>But is that something you would do?</p>
